bool bfs(map<string, set<vector<string>>> university, string from, string to){
    if(from==to){
        return true;
    }
    set<string> visited;
    list<string> queue;
    visited.insert(from);
    queue.push_back(from);
    list<string>::iterator i;
    while(!queue.empty()){
        string current = queue.front();
        queue.pop_front();
        set<vector<string>>::iterator it = university[current].begin();
        while(it!=university[current].end()){
            if(((*it).size()==2)&&((*it)[1].length()>1)){
                if((*it)[1]==to){
                    return true;
                }
                if((visited.find((*it)[1])==visited.end())&&(atoi((*it)[0].c_str())>=0)){
                    visited.insert((*it)[1]);
                    queue.push_back((*it)[1]);
                }
            }
            ++it;
        }
    }
    return false;
}

pair<int, map<string, string>> dijkstra(map<string, vector<vector<string>>> university, string from, string to){
    map<string, string> path;
    map<string, int> dist;
    dist[from] = 0;
    set<pair<int, string>> q;
    q.insert({0, from});
    while(!q.empty()){
        auto top = q.begin();
        string u = top->second;
        q.erase(top);
        for(int i=1; i<university[u].size(); ++i){
            int w = atoi(university[u][i][0].c_str());
            if(w>=0){
                string v = university[u][i][1];
                int dv = INF;
                if(dist.find(v)!=dist.end()){
                    dv = dist[v];
                }
                if(dv > (dist[u]+w)){
                    if(q.find({dv, v})!=q.end()){
                        q.erase(q.find({dv, v}));
                    }
                    path[v] = u;
                    dist[v] = dist[u]+w;
                    q.insert({dist[v], v});
                }
            }
        }
    }
    return pair<int, map<string, string>>(dist[to], path);
}

string getArrow(string from, string to){
    if(from == to){
        return "/\\";
    }
    if(from == "T"){
        if(to == "B"){
            return "\\/";
        }
        else if(to == "L"){
            return "<-";
        }
        else if(to == "R"){
            return "->";
        }
    }
    else if(from == "B"){
        if(to == "T"){
            return "\\/";
        }
        else if(to == "L"){
            return "<-";
        }
        else if(to == "R"){
            return "->";
        }
    }
    else if(from == "L"){
        if(to == "T"){
            return "->";
        }
        else if(to == "B"){
            return "<-";
        }
        else if(to == "R"){
            return "\\/";
        }
    }
    else if(from == "R"){
        if(to == "T"){
            return "<-";
        }
        else if(to == "B"){
            return "->";
        }
        else if(to == "L"){
            return "\\/";
        }
    }
    return "?";
}

